package com.cookandroid.dotnote.util

import android.util.Log

/**
 * 메모 자동 추출 태그 정제 유틸리티
 * 
 * 메모 본문에서 태그를 자동 추출할 때 발생하는 노이즈(조사, 의미 없는 기능어, 불용어)를
 * 효과적으로 제거하여 유의미한 키워드만 태그로 남기도록 필터링합니다.
 */
object TagCleaner {

    // 분석에 의미를 주지 못하는 한국어 기능어, 접속사, 지시어, 용언의 어미 변화 목록
    // 사용자가 지적한 "위해", "위하여", "오랜" 등을 포함하여 대폭 보강함
    val STOP_WORDS = setOf(
        // 접속사 및 부사
        "그리고", "하지만", "그래서", "그러나", "그러면", "즉", "또는", "및", "혹은", "게다가", "따라서", "그러므로", "그렇지만", "반면에",
        "정말", "매우", "너무", "아주", "진짜", "조금", "약간", "많이", "적게", "엄청", "무척", "대단히", "극히", "항상", "언제나", "자주", "가끔", "종종", "늘", "보통",
        // 대명사 및 지시어
        "이것", "저것", "그것", "여기", "거기", "저기", "이렇게", "저렇게", "그렇게", "모든", "다른", "어떤", "무슨", "아무", "여러", "몇", "몇몇", "온갖",
        // 조사 역할을 하거나 문맥상 독자적 의미가 없는 단어들 (사용자 피드백 기반 핵심 필터 대상)
        "위해", "위하여", "위한", "때문에", "때문", "대해", "대하여", "대한", "통해", "통하여", "통한", "관한", "관해", "관하여", "오랜", "오래", "동안", "동안에",
        // 동사/형용사 형태가 남아 있어 명사 태그로 부적절한 잔재
        "있다", "없다", "하다", "되다", "이다", "같아", "같다", "같은", "같음", "같아서", "같은데",
        "하기", "하는", "한", "할", "하였", "했", "했다", "했음", "되거", "되기", "되는", "된", "될", "되었", "됐", "됐다", "됐음",
        "있고", "있는", "있음", "있었", "있었음", "있으", "있으며", "있으나",
        "없고", "없는", "없음", "없었", "없었음", "없으", "없으며", "없으나",
        // 시간 표현 중 일반적인 용어
        "오늘", "내일", "어제", "모레", "그저께", "매일", "매주", "매달", "매년",
        // 의존 명사 및 기타 노이즈
        "등", "등등", "것", "것이", "것을", "것은", "건", "거", "거나", "건가", "것인가", "것임", "것들", "것들의"
    )

    // 조사 제거 정규식: 단어 끝에 붙은 흔한 한국어 조사를 감지하여 제거
    private val JOSA_REGEX = Regex("(은|는|이|가|을|를|도|에|에서|부터|까지|의|로|으로|과|와|한|한테|에게|이랑|랑)$")

    /**
     * 개별 단어의 조사를 제거하고 소문자화하는 정제 함수
     */
    fun cleanTag(rawTag: String): String {
        return try {
            val trimmed = rawTag.trim().lowercase()
            // 조사 제거 수행
            val cleaned = trimmed.replace(JOSA_REGEX, "")
            
            // 조사를 지웠을 때 단어가 너무 짧아지면 원본을 유지하여 명사 훼손 방지
            if (cleaned.length >= 2) cleaned else trimmed
        } catch (e: Exception) {
            Log.e("TagCleaner", "cleanTag 실패: ${e.message}", e)
            rawTag
        }
    }

    /**
     * 텍스트 본문에서 공백 기준으로 단어를 분할하고 정제한 뒤,
     * 불용어를 필터링하여 유효한 태그 목록을 추출합니다.
     */
    fun extractValidTags(content: String, existingTags: List<String> = emptyList()): List<String> {
        return try {
            if (content.isBlank()) return emptyList()

            content
                // 특수문자 제거 후 공백 단위 분절
                .replace(Regex("[^가-힣a-zA-Z0-9\\s]"), " ")
                .split("\\s+".toRegex())
                // 각 단어 정제 (조사 제거)
                .map { cleanTag(it) }
                // 태그 규칙 검증: 길이 2자 이상, 불용어 목록에 없고, 이미 존재하는 태그가 아닌 경우
                .filter { it.length >= 2 && it !in STOP_WORDS && it !in existingTags }
                .distinct()
        } catch (e: Exception) {
            Log.e("TagCleaner", "extractValidTags 실패: ${e.message}", e)
            emptyList()
        }
    }
}
